def seed_blog_posts(apps, schema_editor):
    Topic = apps.get_model("econ", "Topic")
    BlogPost = apps.get_model("econ", "BlogPost")

    for post in BLOG_POSTS:
        blog, _ = BlogPost.objects.update_or_create(
            slug=post["slug"],
            defaults={
                "title": post["title"],
                "excerpt": post["excerpt"],
                "featured_image_filename": post["featured_image_filename"],
                "featured_image_url": post["featured_image_url"],
                "raw_text": "\n\n".join(
                    [
                        f"BLOG {post['order']}: {post['title']}",
                        f"Picture: {post['featured_image_filename']}\n{post['featured_image_url']}",
                        *post["body_paragraphs"],
                        "Sources:\n"
                        + "\n\n".join(
                            f"{source['label']}\n{source['url']}" for source in post["sources"]
                        ),
                    ]
                ),
                "body_paragraphs": post["body_paragraphs"],
                "keywords": post["keywords"],
                "highlights": post["highlights"],
                "gallery": post["gallery"],
                "sources": post["sources"],
                "order": post["order"],
            },
        )
        blog.topics.set(Topic.objects.filter(key__in=post["topic_keys"]))


def unseed_blog_posts(apps, schema_editor):
    BlogPost = apps.get_model("econ", "BlogPost")
    BlogPost.objects.filter(slug__in=[post["slug"] for post in BLOG_POSTS]).delete()


